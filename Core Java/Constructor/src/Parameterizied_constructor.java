//Parameterize Constructor with no Arguments
	 	class Ex2{
			int x;
			int y;
			public Ex2()
			{
				x=10;
				y=20;
			}
			public  void display()
			{
				System.out.println("X="+x);
				System.out.println("Y="+y);
			}
		}
public class Parameterizied_constructor {

	public static void main(String[] args) {
						Ex2 e1=new Ex2();
				e1.display();
			}
}

	
